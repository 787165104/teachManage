package com.teachManage.model;

public class PositionalTitle {
	
	private Integer id;
	
	private String positionalTitleId;
	
	private String positionalTitle;
	

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getPositionalTitleId() {
		return positionalTitleId;
	}

	public void setPositionalTitleId(String positionalTitleId) {
		this.positionalTitleId = positionalTitleId;
	}

	public String getPositionalTitle() {
		return positionalTitle;
	}

	public void setPositionalTitle(String positionalTitle) {
		this.positionalTitle = positionalTitle;
	}


}
